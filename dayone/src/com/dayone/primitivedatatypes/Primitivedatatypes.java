package com.dayone.primitivedatatypes;

public class Primitivedatatypes {
	public static void main(String[] args)
	{
		//declaration datatypes
		//short
		short s =4227;
		System.out.println(s);
		//long
		long l =112345l;
		System.out.println(l);
//char
		char ch='a'+10;
		System.out.println(ch);
		//float
		float f=3245.2354f;
		System.out.print(f);
	}

}
